int	ft_sqrt(int nb)
{
	int	j;
	int	i;

	if (nb <= 0)
		return (0);
	j = 1;
	i = 1;
	while (nb - i > 2)
	{
		j++;
		i += 2;
		nb = nb - i;
	}
	return (j);
}
